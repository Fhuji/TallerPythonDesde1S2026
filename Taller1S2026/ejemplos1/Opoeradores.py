# Ejemplo de operadores aritméticos en Python

a = 10   # 'a' guarda el número 10
b = 3    # 'b' guarda el número 3

# Suma: +
print(a + b)   # Muestra 13 (10 + 3)

# Resta: -
print(a - b)   # Muestra 7  (10 - 3)

# Multiplicación: *
print(a * b)   # Muestra 30 (10 * 3)

# División: /
print(a / b)   # Muestra 3.3333333333333335 (división exacta, da decimal)

# División entera: //
print(a // b)  # Muestra 3 (cociente sin decimales, 10 // 3 = 3)

# Módulo (residuo): %
print(a % b)   # Muestra 1 (el residuo de dividir 10 entre 3 es 1)

# Potencia: **
print(a ** b)  # Muestra 1000 (10 elevado a la 3)

# Resumen:
# Este código realiza operaciones matemáticas básicas con dos números:
# suma, resta, multiplicación, división, división entera, residuo y potencia.
# Cada operación se muestra con print().