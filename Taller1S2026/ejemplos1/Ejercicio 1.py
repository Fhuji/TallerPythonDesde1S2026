# Ejercicio: Crear variables personales y mostrar una presentación

nombre = 'María'        # Guarda el nombre
edad = 22               # Guarda la edad
ciudad = 'Antigua'      # Guarda la ciudad favorita

# Mostrar una frase con los datos
# print puede recibir varios valores separados por comas, y los muestra con espacios
print('Me llamo', nombre, ', tengo', edad, 'años y soy de', ciudad)

# Resumen:
# Este ejercicio crea variables con información personal y luego las usa
# dentro de un print para formar una presentación. Las comas en print
# separan los elementos y añaden espacios automáticamente.