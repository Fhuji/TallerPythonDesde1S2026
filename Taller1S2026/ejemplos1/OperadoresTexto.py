# Ejemplo de operaciones con texto en Python

nombre = 'Carlos'       # Variable con texto "Carlos"
apellido = 'García'     # Variable con texto "García"

# Concatenar (unir) textos con el operador +
nombre_completo = nombre + ' ' + apellido   # Une nombre, espacio y apellido
print(nombre_completo)   # Muestra: Carlos García

# Repetir texto con el operador *
print('-' * 20)          # Muestra 20 guiones: --------------------

# Resumen:
# El signo + también sirve para unir textos (concatenar). El signo *
# repite un texto la cantidad de veces indicada.