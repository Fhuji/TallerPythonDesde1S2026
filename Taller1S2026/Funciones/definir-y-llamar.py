

# Ejemplo de función simple sin parámetros

# Definir una función llamada saludar
def saludar():
    print('¡Hola! Bienvenido al taller')

# Llamar a la función varias veces
saludar()   # Ejecuta el código dentro de saludar
saludar()
saludar()

# Resumen:
# def sirve para definir una función. Una función agrupa código que
# queremos reutilizar. Para ejecutarla, escribimos su nombre seguido
# de paréntesis.